package com.zjl.designmodel.factory.simple;

public class Concept {
	/**
	 * 1.简单工厂模式不属于23种设计模式，简单工厂一般分为：普通简单工厂、多方法简单工厂、静态方法简单工厂
	 * 2.普通简单工厂:建立一个工厂类，对实现了同一接口的一些类进行实例的创建
	 * 3.多个方法简单工厂：是对普通工厂方法模式的改进，在普通工厂方法模式中，如果传递的字符串出错，则不能正确创建对象，而多个工厂方法模式是提供多个工厂方法，分别创建对象
	 * 4.多个静态方法简单工厂：多个工厂方法模式里的方法置为静态的，不需要创建实例，直接调用即可
	 * 
	 */
}
